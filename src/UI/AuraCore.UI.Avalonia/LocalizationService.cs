using System.Text.Json;

namespace AuraCore.UI.Avalonia;

/// <summary>
/// Centralized localization service for Avalonia UI.
/// Mirrors WinUI3 S._() system. Key format: "section.key"
/// </summary>
public static class LocalizationService
{
    public static string CurrentLanguage { get; private set; } = "en";

    private static readonly string ConfigPath = Path.Combine(
        Environment.GetFolderPath(Environment.SpecialFolder.LocalApplicationData),
        "AuraCorePro", "language.json");

    /// <summary>Get localized string by key. Falls back to key itself.</summary>
    public static string Get(string key)
    {
        var dict = CurrentLanguage == "tr" ? TR : EN;
        return dict.TryGetValue(key, out var val) ? val : (EN.TryGetValue(key, out var en) ? en : key);
    }

    /// <summary>Shortcut alias</summary>
    public static string _(string key) => Get(key);

    public static void SetLanguage(string lang)
    {
        lang = lang.ToLowerInvariant();
        if (lang != "en" && lang != "tr") lang = "en";
        if (CurrentLanguage == lang) return;
        CurrentLanguage = lang;
        Save();
        LanguageChanged?.Invoke();
    }

    public static event Action? LanguageChanged;

    public static void Load()
    {
        try
        {
            if (!File.Exists(ConfigPath)) return;
            var json = File.ReadAllText(ConfigPath);
            using var doc = JsonDocument.Parse(json);
            if (doc.RootElement.TryGetProperty("Lang", out var prop))
            {
                var v = prop.GetString()?.ToLowerInvariant();
                if (v == "tr") CurrentLanguage = "tr";
            }
        }
        catch { /* default to en */ }
    }

